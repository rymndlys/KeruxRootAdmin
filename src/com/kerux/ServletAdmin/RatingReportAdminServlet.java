@WebServlet("/RatingReportAdminServlet")
public class RatingReportAdminServlet extends HttpServlet implements DBUtilityAdmin {
	private static final long serialVersionUID = 1L;
       
	private static Connection getConnection(){
		Connection connection = null;
		
		try{
			Class.forName(Security.decrypt(jdbcDriverName)); //ensure that the library will be loaded to the memory
			//secure database connection string configuration
			connection = DriverManager.getConnection(Security.decrypt(jdbcUrl),
					Security.decrypt(dbUserName),Security.decrypt(dbPassword));
			
		}catch(ClassNotFoundException cfne){
			//should be displayed on a secure error page code
			System.err.println(cfne.getMessage());
		}catch(SQLException sqle){
			System.err.println(sqle.getMessage());
		}
		return connection;
	}
	
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException, ClassNotFoundException, SQLException {
        	try {
        		Security sec = new Security();
        		 Connection con=getConnection();
                 response.setStatus(HttpServletResponse.SC_OK);
                 OutputStreamWriter writer = new OutputStreamWriter(response.getOutputStream());
                 
                 String query1 = SELECT_AVE_RATING;

                 PreparedStatement ps1 = con.prepareStatement(query1);
                 ResultSet rs=ps1.executeQuery();

                 while (rs.next()) {
                  	writer.write(rs.getString(1)+"\n");

                 }
                 
                 
                 PreparedStatement st = con.prepareStatement(SELECT_RATING);
                 ResultSet rset = st.executeQuery();

                 while (rset.next()) {
                	 writer.write(rset.getString(1)+" | ");
                  	writer.write(rset.getString(3)+", "+rset.getString(2)+" | ");
                  	writer.write(rset.getString(4)+"\n");

                 }


                 writer.flush();
                 writer.close();
                
            } catch (IOException e) {
                try{
                    response.setStatus(HttpServletResponse.SC_BAD_REQUEST);
                    response.getWriter().print(e.getMessage());
                    response.getWriter().close();
                } catch (IOException ioe) {
                }
            }  
        	
           

        
    }

    
    
	protected void doGet(HttpServletRequest request, HttpServletResponse response)  throws ServletException, IOException {
	    try {
	        processRequest(request, response);
	    } catch (ClassNotFoundException ex) {
	        Logger.getLogger(AndroidServlet.class.getName()).log(Level.SEVERE, null, ex);
	    } catch (SQLException ex) {
	        Logger.getLogger(AndroidServlet.class.getName()).log(Level.SEVERE, null, ex);
	    }
	}


	protected void doPost(HttpServletRequest request, HttpServletResponse response)  throws ServletException, IOException {
	    try {
	        processRequest(request, response);
	    } catch (ClassNotFoundException ex) {
	        Logger.getLogger(AndroidServlet.class.getName()).log(Level.SEVERE, null, ex);
	    } catch (SQLException ex) {
	        Logger.getLogger(AndroidServlet.class.getName()).log(Level.SEVERE, null, ex);
	    }
	}

}